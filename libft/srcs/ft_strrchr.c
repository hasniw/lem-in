#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i])
	{
		i++;
	}
	while ((c != s[i]) && s[i] != s[0])
		i--;
	if (c == s[i])
		return ((char *)&s[i]);
	return (0);
}
