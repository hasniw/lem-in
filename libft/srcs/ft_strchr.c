char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*a;

	a = (char *)s;
	i = 0;
	while ((c != a[i]) && a[i])
		i++;
	if (c == a[i])
		return (&a[i]);
	return (0);
}
