#include "libft.h"

int		ft_isint(char *s)
{
	size_t i;

	i = 0;
	if (s[0] == '-' && !s[1])
		return (0);
	while (s[i])
	{
		if (s[0] == '-')
			i++;
		if (!ft_isdigit(s[i]))
			break ;
		i++;
	}
	return (i == ft_strlen(s));
}
